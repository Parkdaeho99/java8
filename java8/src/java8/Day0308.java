package java8;

public class Day0308 {

	public static void main(String[] args) {
		// TODO 자동 생성된 메소드 스텁
		int a = 10;
		System.out.println(a);
		a += 1;
		System.out.println(a);
		a += 1;
		System.out.println(a);
		a += 1;
		System.out.println(a);
		
		a++;
		System.out.println(a);
		a++;
		System.out.println(a);
		a++;
		System.out.println(a);
		
		++a;
		System.out.println(a);
		++a;
		System.out.println(a);
		++a;
		System.out.println(a);
		
		a--;
		System.out.println(a);
		a--;
		System.out.println(a);
		a--;
		System.out.println(a);
		
		a=10;
		System.out.println();
		System.out.println(++a);
		System.out.println(a++);
		System.out.println(a);
	}

}
